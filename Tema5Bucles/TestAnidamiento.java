public class TestAnidamiento {
    public static void main(String[] args) {
        int lado1 = 20;
        int lado2 = 15;
        String caracter = "*";

        for (int i = 0; i < lado1; i++) {
            for (int j = 0; j < lado2; j++) {
                System.out.print(caracter);
            }
            System.out.println();
        } 

    }
}
